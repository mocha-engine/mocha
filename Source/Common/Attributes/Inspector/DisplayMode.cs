namespace Mocha.Common;

/// <summary>
/// Defines a mode for a property editor to display itself.
/// </summary>
public enum DisplayMode
{
	/// <summary>
	///  Simple text input.
	/// </summary>
	Text,
	/// <summary>
	/// An element you can drag to change value.
	/// </summary>
	Drag,
	/// <summary>
	/// An element with a slidable element to change value.
	/// </summary>
	Slider
}
